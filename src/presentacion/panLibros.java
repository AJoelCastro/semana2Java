    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Obtener el c√≥digo del libro a eliminar desde un campo de texto de b√∫squeda o selecci√≥n
        String codigoLibro = txtBusqueda.getText();  // Por ejemplo, si se ingresa el c√≥digo en un campo de texto

        if (codigoLibro.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingresa un c√≥digo de libro v√°lido.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Mostrar el di√°logo de confirmaci√≥n
            int confirmacion = JOptionPane.showConfirmDialog(
                this, 
                "¬øEst√°s seguro de que deseas eliminar el libro con c√≥digo: " + codigoLibro + "?", 
                "Confirmar Eliminaci√≥n", 
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
            );

            // Si el usuario confirma la eliminaci√≥n
            if (confirmacion == JOptionPane.YES_OPTION) {
                try {
                    // Llamar al m√©todo para eliminar el libro
                    ListaLibros.eliminarLibro(codigoLibro);
                    JOptionPane.showMessageDialog(this, "Libro eliminado correctamente.", "√âxito", JOptionPane.INFORMATION_MESSAGE);

                    // Actualizar la tabla despu√©s de la eliminaci√≥n
                    DefaultTableModel modelo = new DefaultTableModel();
                    String[] columnas = {"ID", "C√≥digo", "T√≠tulo", "Autor", "Categor√≠a", "A√±o", "Copias Disponibles", "Editorial"};
                    modelo.setColumnIdentifiers(columnas);
                    ListaLibros.listarTodos(modelo);
                    jTable1.setModel(modelo);
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(this, "Error al eliminar el libro: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

